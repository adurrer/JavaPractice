
public class Variables {
	public static void main(String[] args) {
		int integerNum = 1;
		double doubleNum = 1.0202020;
		float floatNum = 1.1F;
		char key = 'C';
		String stringVar = "String Variable";
		System.out.println("integerNum is int that equals " + integerNum + "\n");
		System.out.println("doubleNum is double that equals " + doubleNum + "\n");
		System.out.println("floatNum is float that equals " + floatNum + "\n");
		System.out.println("key is char that equals " + key + "\n");
		System.out.println("stringVar is String that equals " + stringVar + "\n");
	}
}
